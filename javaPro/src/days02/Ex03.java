public class Ex03 {

	public static void main(String[] args) {
		// println() 메서드 - 출력
		System.out.println("이창익");
		System.out.println("송세호");
		System.out.println("이시훈");

		// print() 메서드 - 출력
		System.out.print("김재민"+ '\n');
		System.out.print("김재민\n");
		System.out.print("김재민"+ '\r');
		System.out.print("김재민");
		System.out.println(); // 개행
		System.out.print("김재민");

		// 줄바꿈(개행) 여부 == CR '\r' + LF '\n'

	}

}







